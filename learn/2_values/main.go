package main

import "fmt"

func main() {
	// int
	fmt.Println(1)
	fmt.Println(1 + 1)
	fmt.Println(10 % 2)

	// string
	fmt.Println("1" + "1")
	fmt.Println("hello from main func")

	// bool
	fmt.Println(true)
	fmt.Println(false)

	// floats
	fmt.Println(10.5)
	fmt.Println(10.5 + 2)
	fmt.Println(10.5 - 2)
	fmt.Println(10.5 * 2)
	fmt.Println(10.5 / 2)
}
